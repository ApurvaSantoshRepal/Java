//Write a program , take a number and print wether it is less than 10 or greater than 10
class Number{
	public static void main(String[] args){
		int var = 10;
		if (var ==10){
			System.out.println(var+" is equal to 10");
		}
		else if (var >10){
			System.out.println(var+ " is greater than 10");
		}
		else{
			System.out.println(var +"is less than 10");
		}
	}
}

